package classesMethodsConcepts;

public class VolumeOfCuboid
{
	
	
    public static void main(String[] args) {
    int length = 5;
    int breadth = 4;
    int height = 3; 
    int volume;
    volume = length*breadth*height;
    System.out.println("Length of the cuboid is"+length);
    System.out.println("Breadth of the cuboid is"+breadth);
    System.out.println("Height of the cuboid is"+height);
    System.out.println("Volume of the cuboid is"+volume);

  }
}
