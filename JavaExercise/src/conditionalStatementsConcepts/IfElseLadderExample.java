package conditionalStatementsConcepts;

public class IfElseLadderExample

{
	    public static void main(String[] args) {
	        int a = -6;

	        if (a == 0) {
	            System.out.println("Value of a is zero");
	        } else if (a == 1) {
	            System.out.println("Value of a is One");
	        } else if (a == 2) {
	            System.out.println("Value of a is Two");
	        } else if (a == 3) {
	            System.out.println("Value of a is Three");
	        } else if (a == 4) {
	            System.out.println("Value of a is Four");
	        } else if (a == 5) {
	            System.out.println("Value of a is Five");
	        } else if (a == 6) {
	            System.out.println("Value of a is Six");
	        } else {
	            System.out.println("Value is negative or not matched");
	        }
	    }
	}



