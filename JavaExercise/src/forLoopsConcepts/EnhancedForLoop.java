package forLoopsConcepts;

public class EnhancedForLoop {

	 public static void main(String[] args) {
	        int[] numbers = {10, 20, 30, 40, 50};

	        for (int num : numbers) {
	            System.out.println(num);
	        }
	    }
	}
//Enhanced For Loop:

//Iterates over each element of the numbers array directly, without using an index.
//Prints the value of the current element number.