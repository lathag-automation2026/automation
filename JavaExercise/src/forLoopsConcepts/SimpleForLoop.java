package forLoopsConcepts;

public class SimpleForLoop {

	 public static void main(String[] args) {
	        for (int i = 1; i <= 10; i++) {
	            System.out.println(i);
	        }
	    }
	}

//Explanation:
//Simple For Loop:

//Initializes a counter variable i to 1.
//Checks if i is less than or equal to 10.
//Increments i by 1 in each iteration.
//Prints the value of i in each iteration.