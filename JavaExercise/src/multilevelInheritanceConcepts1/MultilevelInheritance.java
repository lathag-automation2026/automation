package multilevelInheritanceConcepts1;

public class MultilevelInheritance 
{
	 public static void main(String[] args) {
	       
	        C cobj = new C();
	        cobj.print();
	        cobj.display();
	        cobj.show();
	        
	        System.out.println("--------");

	        System.out.println(cobj.a);
	        System.out.println(cobj.b);
	        System.out.println(cobj.c);

	 }
}
