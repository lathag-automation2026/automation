package switchCaseConcepts;

public class IfConvertToSwitchExampleCheckGender 
{
	public static void main(String[] args) {
	       char gender = 'M';
	       
	       if(gender =='M'){
	           System.out.println("male"); 
	       } else if(gender =='F'){
	           System.out.println("female");
	       } else {
	           System.out.println("invalid gender");
	       }
	       
	       
	}
}
